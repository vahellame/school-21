char	*ft_strcat(char *str1, const char *str2)
{
	int i;
	int k;

	i = 0;
	while (str1[i])
		i++;
	k = 0;
	while (str2[k])
	{
		str1[i] = str2[k];
		i++;
		k++;
	}
	str1[i] = '\0';
	return (str1);
}
