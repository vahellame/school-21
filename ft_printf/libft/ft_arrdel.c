#include <stdlib.h>

void	ft_arrdel(char ***arr)
{
	size_t	i;

	if (*arr)
	{
		i = 0;
		while ((*arr)[i])
		{
			free((*arr)[i]);
			i++;
		}
		free(*arr);
		(*arr) = NULL;
	}
}
