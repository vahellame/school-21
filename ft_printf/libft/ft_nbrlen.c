#include "stdlib.h"

size_t	ft_nbrlen(long long int n)
{
	size_t	size;

	size = 0;
	if (n < 0)
		size++;
	while (n / 10 != 0)
	{
		n = n / 10;
		size++;
	}
	size++;
	return (size);
}
